#define _crt_secure_no_warnings
#include <stdio.h>
#include <stdlib.h>

int main(void) {
	FILE *in_file;
	int i, j, k;
	int no_cases, no_data1, no_data2;
	int temp;

	in_file = fopen("input.txt", "r"); /*파일 열기*/
	if (in_file == NULL) exit(1);/*빈 파일일시 끝내기*/

	fscanf(in_file, "%d", &no_cases); /*케이스개수 받기*/

	
	for (i = 1; i <= no_cases; i++)/*케이스 갯수만큼 코드 실행*/
	{
		fscanf(in_file, "%d %d", &no_data1, &no_data2);/*받는 숫자의 갯수 받기*/
		
		
		for (j = 1; j <= no_data2; j++)/*받는숫자의 갯수만큼 실행*/
		{
			temp = j;
			for (k = 1; k <= no_data1; k++) {
				printf("%d ", temp);
				temp += no_data2;
			}
			printf("\n");
		}
	}
	fclose(in_file);
	return 0;
}