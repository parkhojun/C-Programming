#define _crt_secure_no_warnings
#include <stdio.h>
#include <stdlib.h>

int main(void) {
	FILE *in_file;
	int i, j, k ;
	int no_cases, no_data;
	int data,data2;
	int *a;
	int temp;

	in_file = fopen("input.txt", "r"); /*파일 열기*/
	if (in_file == NULL) exit(1);/*빈 파일일시 끝내기*/

	fscanf(in_file, "%d", &no_cases); /*케이스개수 받기*/

	for (i = 0; i < no_cases; i++)/*케이스 갯수만큼 코드 실행*/
	{
		fscanf(in_file, "%d", &no_data);/*받는 숫자의 갯수 받기*/
		
		a = (int *)malloc(sizeof(int)*no_data);

		for (j = 0; j < no_data; j++)/*받는숫자의 갯수만큼 실행*/
		{
			fscanf(in_file, "%d", &data); /*받는숫자 받기*/

			a[j]=data;
		}/*받는숫자의 갯수만큼 실행 끝부분*/
		fscanf(in_file, "%d", &data2);
		printf("%d ", no_data - data2 + 1);
		for (j = 0; j < no_data-data2+1; j++) {
			temp = 0;
			for (k = 0; k < data2; k++) {

				temp += a[k+j];
			}
			temp /= data2;
			printf("%d ", temp);
		}

		printf("\n");
	}
	free(a);
	fclose(in_file);
	return 0;
}